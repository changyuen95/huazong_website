            <p><b>华总-Carlsberg教育基金助学金申请细则</b></p>



            <p>华总教育总基金委员会于9/3/2023与Carlsberg Malaysia 管理层举行线上视频会议，讨论了有关进行第二次颁发助学金于贫困家庭之大学及大专生之事宜。</p>



            <p>这项总额为100万令吉助学金计划，经已在3/9/2023的华总代表大会上获得接纳和通过。在此计划下，将有333名在籍的清寒大学及大专生受惠，每人可得RM3000助学金。</p>



            <p style="font-weight: bold;">1）助学金申请细则</p>



            <p style="font-weight: bold;">1.1 申请资格：</p>
            <ul style="list-style-type: none;">
                <li>i.  马来西亚公民。</li>
                <li>ii.  在籍国内学院/大专生或大学生。</li>
            </ul>   

            <p style="font-weight: bold;">1.2. 申请条件：</p>
            <ul style="list-style-type: none;">
                <li>i.        单亲家庭或；</li>
                <li>ii.       双亲皆失业或；</li>
                <li>iii.      孤儿或；</li>
                <li>iv.       家境清寒，低收入群体（B40）</li>
                <li>v.       残障家庭。</li>
            </ul> 

            <p style="font-weight: bold;">1.3. 申请文件：</p>
            <ul style="list-style-type: none;">
                <li>i.         填写由 华总-Carlsberg 教育基金委员会发出的申请表格 (可到华总网站www.dama-huazong.com下载)；</li>
                <li>ii.       申请者（学生）身份证副本；</li>
                <li>iii.      家长/监护人身份证副本；</li>
                <li>iv.      学生证、学业成绩单、或学府正式录取通知书副本（凡可以核实证明身          份的文件皆可提交)；</li>
                <li>v.       单亲家庭与孤儿需提交双亲离婚证书或死亡证书或相关证明文件副本；</li>
                <li>vi.      双亲皆失业或家境清寒需提交有关家庭经济状况资料，如获得低收入群          体 （B40）援助金、在职者每月工资单（Pay Slip）、雇主发予员工的报          税表（EA Form）、雇员公积金（EPF） 或其他相关证明文件或；</li>
                <li>vii.      任何无法提交文件证明的小贩或自雇人士的孩子们, 可以提交由校长或相关州属的中华大会堂总会长的推荐证明书。</li>
            </ul> 

            <p style="font-weight: bold;">1.4. 受惠人数与助学金额： 共 333 名，每人 3000 令吉。</p>

            <p style="font-weight: bold;">1.5  申请、评选及审核日期：</p>
            <ul style="list-style-type: none;">
                <li>i.         申请：2024 年 1 月 11 日 至 2024 年 2 月 29 日截止（逾期恕不受理）</li>
                <li>ii.        各州华堂评选及审核：2024年3月1日至13日</li>
                <li>iii.       华总-Carlsberg教育基金最后审核：2024年3月14日至29日</li>
            </ul> 
            
            <p style="font-weight: bold;">1.6.     名单公布日期：2024年4月4日</p>
            <p style="font-weight: bold;">1.7.     颁发日期： 2024 年 4 月 21日（星期日）</p>
            <p style="font-weight: bold;">1.8. 审核：</p>
            <ul style="list-style-type: none;">
                <li>i.         由各州中华大会堂负责审核遴选，并把获选学生名单推荐予 华总-              Carlsberg 教育基金委员会作最终审定，申请者任何质询恕不受理。</li>
                <li> ii.       合格申请者将接获各州中华大会堂或华总的通知。</li>
            </ul> 

            <p style="font-weight: bold;">1.9. 步骤：</p>
            <ul style="list-style-type: none;">
                <li>i.        申请者详读细则，下载并填写表格；</li>
                <li>ii.        申请者准备各类文件副本；</li>
                <li>iii.      申请者提交表格及各文件副本予各州中华大会堂或华总；</li>
                <li>iv.       申请文件可通过email或邮寄至各州中华大会堂或华总收取；</li>
                <li>v.       申请从2024 年 1 月 1 1日开始至 2024 年 2 月 29日截止，逾期 或资料不       齐全者一概不受理；</li>
                <li>vi.       各州中华大会堂各自召开遴选会议并选出初选人数，并于 2024年 3月           14日 或之前，提呈初选名单予华总- Carlsberg 教育基金委员会；</li>
                <li>vii.      华总-Carlsberg 教育基金委员会作最后审定，并于 2024 年 4 月 4 日 公布       最终名单；</li>
                <li>viii.     成功获取助学金的学生总名单将在各大报章公布，同时也通过华总官方          网站与面子书专页公布与通知；</li>
                <li>ix.      成功申请者必须遵从各州中华大会堂或华总的指示，出席颁发典礼并亲          自领取助学金；</li>
                <li>x.       2024 年 4月21 日（星期日）举行颁发助学金典礼。华总及13州华堂将          同步进行这项颁发仪式仪式。</li>
            </ul> 
            
            <p style="font-weight: bold;">2）申请、评选及頒发助学金时间表</p>
            
            <p style="font-weight: bold;">11/1/2024  - 29/2/2024      - 接受申请</p>
            <p style="font-weight: bold;">1/3/2024 - 13/3/3024        - 各华堂及单位开始进行初审及筛选工作</p>
