        <h4 class="font-weight-bolder mb-3">简介</h4>
        <p class="font-weight-bold">
            1978年10月15日，森美兰中华总商会、福建会馆、广东会馆、广西会馆、华校董联会主催召开全森华人注册社团代表大会，大会在芙蓉华济公会礼堂召开，出席团体代表达150人。会上一致通过成立森美兰中华大会堂的建议，并选出十五人筹委会，全权负责起草章程以及进行注册工作。会上，亦选出五人筹备秘书团。同年11月26日，召开第二次全森华人注册社团代表大会，通过章程草案。
        </p>
        <p class="font-weight-bold">
            经过四年多的筹备，森美兰中华大会堂终于成功在1982年1月获准注册，并于同年11月14日召开第一次会员大会。森美兰中华大会堂于1983年元旦举行成立典礼，恭请雪州中华大会堂会长兼马来西亚中华大会堂联合会筹委会主席邱祥炽先生主持开幕。
        </p>
        <p class="font-weight-bold">
        <b>作为森州华社最高领导机构，森美兰中华大会堂的宗旨为：</b>
        </p>
            <ul>
                <li>联合森州各华人注册社团，促进情谊，增进福利，赞助社会慈善公益，排难解纷。</li>
                <li>与国内拥有相同宗旨之其他华人注册社团联络。</li>
                <li>赞助文化、教育、科学、工艺事业、推动文娱、音乐、体育之活动。</li>
            </ul>
        <p class="font-weight-bold">
            森美兰中华大会堂的使命为成为具有前瞻性、现代化，民主且卓越的模范组织，积极领导森州华社建设文教事业，维护族群权益，参与国家发展，实现公平民主社会。
        </p>
        <p class="font-weight-bold">
        <b>森美兰中华大会堂的目标则包括：</b>
        </p>

        <ol>
            <li>培训森州华团领袖</li>
            <li>提升行政工作效率</li>
            <li>成立研究开发小组</li>
            <li>支持华人民办学府</li>
            <li>推动华族文化</li>
            <li>参与华族文化的兴革</li>
            <li>推广婚姻辅导</li>
            <li>提升华团的素质</li>
            
        </ol>

