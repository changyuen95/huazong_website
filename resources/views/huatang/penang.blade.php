        <h4 class="font-weight-bolder mb-3">简介</h4>
        <p class="font-weight-bold">
            槟州华人大会堂是槟州华社最高民间机构，属下会员包括州内华人社团及华裔同胞。
        </p>
        <p class="font-weight-bold">
            槟州华人大会堂前身为平章会馆，乃於1875年依据槟城海峡殖民地政府颁发的一份信托证书而成立的。该份信托证书拨给一块位於槟城椰脚街地段，指定在此地段建立一座建筑物名为“华人大会堂(Chinese Town Hall)”。当时，殖民地政府的目的在于协调华侨的私会党争与民间纠纷，为殖民地政府维持社会秩序及巩固统治权力，并非十足维护华侨的共同利益。
        </p>
        <p class="font-weight-bold">
           七十年代，槟州华社展开复兴及重组平章会馆运动。于1974年，当时的槟州首席部长敦林苍佑医生呼吁全槟华人和社团应立下决心，复兴平章会馆，作为华社表达社会及经济愿望的中心以及团结华人的力量。
        </p>
        <p class="font-weight-bold">
            1974年平章会馆改选后，在新任主席拿督庄汉良局绅领导下，展开全面革新与进行重组，改名为槟州华人大会堂。1975年，槟州华人大会堂筹建十层大厦，获各界热烈响应，各阶层竞相献捐。1978年12月19日举行大厦奠基典礼，并于1983年新厦宣告落成。
        </p>    
