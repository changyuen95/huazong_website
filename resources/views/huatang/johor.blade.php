        <h4 class="font-weight-bolder mb-3">简介</h4>
        <p class="font-weight-bold">
            柔佛州中华总会创立于1946年，当时名为“柔佛华侨总会”。1946年1月16日，由李国华先生主催召集全柔各地代表齐集峇株中华商会，会商组织总会，全体代表一致赞成组织一个全柔华人最高领导机关－“华侨总会”，以办理全柔数十万华侨福利事业，与政府合作，促进各民族和睦相处。会址乃设在峇株中华商会，至1946年9月18日始易名为“柔佛州中华总会”。
        </p>
        <p class="font-weight-bold">
            1941年，世界第二次大战，日寇南侵，由哥打峇鲁登陆，直捣柔佛防线，奸杀掳掠，惨无人道，祸延无辜百姓，人民生活在水深火热中，日军占领三年八个月，杀害忠烈爱国之士，众多义忠之侨领，因而壮烈牺牲。
        </p>
        <p class="font-weight-bold">
            光复后，殉难先烈忠魂分散四处，柔佛州中华总会同人为纪念先烈为国，为民族而牺牲的精神，乃向政府申请一块位于亚依淡的地段以埋葬殉难先烈骸骨。总会成立亚依淡殉难侨胞公墓筹建小组委员会，共有十五名委员，全部建筑费约为二万元，由各区分担。每年3月17日，柔佛州中华总会及各地华团都齐集于亚依淡举行盛大公祭。
         </p>
        <p class="font-weight-bold">
            1950年，本邦公民权法例颁布施行，唯华人同胞有少数思想陈旧，固步自封，未明当前趋势，倘非获得居留地之公民权，一切权益安得保障。当时，有资格申请为公民之华人有二十万人，但申请者却不及半数，为着未来前途着想，柔佛州中华总会联合马华公会推动鼓励及协助华人申请联邦公民或州籍民，这一项功绩是不可抹煞的。
        </p>  
        <p class="font-weight-bold">
            本邦暴乱初起时，柔佛州各县所有冤怨事件，皆由中华总会联合各地中华公会及中华商会，力加营救。由于柔佛州乃最先发生暴乱之一州，因之事件特别繁多复杂，侨胞损失重大。柔佛州中华总会因势利导，乃发函本州各县中华公会及中华商会推派代表，于1949年3月2日在峇株中华商会举行代表大会，商组马华公会柔佛分会事宜。结果大会顺利举行产生职员，会议也议决通过各县委员召集本地华人注册社团代表会议组织各县支会，共同维护本邦治安，并拯救同胞于水深火热之中。
        </p>  
        <p class="font-weight-bold">
            随着柔州马华公会组织成功，本总会责任已减轻，但本总会仍坚定意志，团结一致，会务继续发展，为民众服务。本总会对华文教育，亦非常重视，为华教略尽绵力，在本州首创全马之董教联合会，以谋董教之合作，从而发展中华文化。在促进体育方面，柔佛州中华总会曾主办第四届全柔运动会，派遣男女篮球队参加南大杯义赛，协助全国篮球队参加介寿杯赛，以石吾侨教育体育平衡发展。在社会福利方面，1947年，郑潮炯先生在全柔义卖，共为总会筹得义款二万二千余元。总会除拨一万元汇交中国救济孤儿伤难，其余之义款则存于柔佛州中华总会为本邦福利基金。1984年初由总会拨出义款救济麻属武吉港脚火灾难侨、居銮火灾、哥打丁宜火灾、峇株难侨等。
        </p>
