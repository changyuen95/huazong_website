        <h4 class="font-weight-bolder mb-3">简介</h4>
        <p class="font-weight-bold">
            联络及团结砂州各省华人社团以谋求我国各阶层人民之福利与进步；关注、研究与促进我国之经济、文化、教育、慈善公益等事业，以及民族间之和谐；促进及维护砂州华裔社会之政治、经济、文化和教育上的权益。
        </p>
        <p class="font-weight-bold">
            本会在砂州华人社团全力支持与合作之下，于1983年7月17日宣告诞生。这个被誉为砂州华人社团最高领导机构，目前包括八个成员单位，即：晋汉省华人社团总会、斯里阿曼及木中省华人社团联合会、诗巫省华人社团联合会、美里省华人社团联合会、泗里奎省华人社团联合会、民都鲁省华人社团联合会、加帛省华人社团联合会及砂拉越林梦省华人社团联合会，由这些省级华总派出代表组成本会理事会。
        </p>
        <p class="font-weight-bold">
            本会自成立以来即扮演对上秉承马来西亚华总之号召，对下联系砂州各省华总的角色，朝向大团结的目标迈进；在此岗位上举办了涵盖文化、教育、社会、经济、福利等范畴的活动。
