        <h4 class="font-weight-bolder mb-3">简介</h4>
        <p class="font-weight-bold">
            为了筹组一个能够反映华社经济、教育、文化、福利暨宗教的机构,沙巴商联会会长拿督陈友仁局绅,以嘉达山人、杜顺人都有其文化传统特征的组织和大厦,
            而华社则无组织代表华人社会,以及团结华社,并藉以协助解决华社的共同问题,维护华族的权益等为由,倡议筹组中华大会堂或类似之组织。
            这个概念,获得亚庇华人同乡会馆联合会原则上赞同。
        </p>
        <p class="font-weight-bold">
            筹组本州中华大会堂为华族千秋大业,沙商联会理监事会议议决巡回全州与各地华人注册社团领导人举行座谈,收集意见,在会长拿督陈友仁局绅率领下,
            商联会代表先后到根地咬(包括丹南)、斗湖(包括仙本那、古纳、拿笃)、山打根、保佛(包括哥打马鲁都)采集了各地华团不同的意见,
            包括需容纳有传统宗教色彩的团体,以文化、经济、福利、教育及宗教为组织基本,所有成员应以民主原则,分享权利和履行义务。
        </p>
        <p class="font-weight-bold">
            1992年12月2日,沙商联会第廿九届第二次执行理事会会议通过,决定向社团注册局申请注册。会议议决于1992年12月28日假亚庇中华工商总会召开“沙巴中华大会堂”筹委会会议
            ,并正式将新组织定名为“沙巴中华大会堂”及接纳修正后的章程草案,并推举十一人筹备委员会负责向社团注册局办理申请注册。
        </p>
        <p class="font-weight-bold">
            1994年2月22日,沙巴中华大会堂的注册获得社团注册局正式批准,注册准证号为18/94(Sabah)。
        </p>    
