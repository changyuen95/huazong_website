        <h4 class="font-weight-bolder mb-3">简介</h4>
        <p class="font-weight-bold">
        马六甲中华大会堂正式成立之前，工作由马六甲中华总商会肩负。1986年开始，中华总商会领导层建议筹组大会堂，以配合十五华团的工作。同年12月，商会致函全甲华团共商成立大会堂事宜，共有四分之三代表出席大会，并一致同意成立大会堂。 1986年12月28日，正式成立筹委会，并准备拟定章程与处理申请注册等事宜。1988年1月9日，马六甲中华大会堂获得社团注册局批准成立。 作为马六甲华团最高领导机构，马六甲中华大会堂的组织宗旨及目标为：
        </p>

        <ol>
            <li>促进马六甲州各华人注册社团及华裔人士之团结联系、增进情谊、谋求福利、进行慈善工作及排难解纷；
</li>
            <li>与国内其他有相同宗旨之华人注册社团保持联系及合作精神；
</li>
            <li>促进文化、教育、科学、工艺事业及推动文娱、体育活动；
</li>
            <li>促进各民族间的和谐及亲善。
</li>
            
            
        </ol>

        <p class="font-weight-bold">
        马六甲中华大会堂的会员共分为两类，即团体会员与个人会员。

        </p>
      
