        <h4 class="font-weight-bolder mb-3">简介</h4>
        <p class="font-weight-bold">
            1978年5月5日，吉兰丹中华总商会负责召开全丹华人注册社团代表联席会议，席间一致议决成立吉兰丹中华大会堂，由每一社团单位派出一名代表担任筹备委员会委员，由当年的中华总商会会长拿督黄昆福医生为召集人。
        </p>
        <p class="font-weight-bold">
            同年11月5日召开第一次筹备会议，正式成立筹备委员会并起草章程。翌年1月12日，雪兰莪中华大会堂前会长丹斯里李延年召集全国各州华团主要负责人，商讨创立“马来西亚中华大会堂联合会”事宜，此举更加速了丹州中华大会堂的筹备工作。
        </p>
        <p class="font-weight-bold">
            1981年5月6日，丹华堂的社团注册终获正式批准，随即在同年10月23日召开选举大会，选出第一届理事会职员。同年11月20日，假中华总商会礼堂举行盛大的成立暨就职典礼，并恭请槟州华人大会堂会长拿督许平等莅临主持监誓礼，场面热烈。
        </p>
        <p class="font-weight-bold">
            随后，乃将永久办事处设于中华总商会内，并接手办理公祭日治时期殉难烈士及联合各社团庆贺每年受封之华裔同胞。        
        </p>    
        <p class="font-weight-bold">
            1985年1月18日，丹州华社一致决定复办中华独中，丹华堂受推为复兴工委会主席，积极展开工作。同时将会堂原拟设立图书馆的场地暂借予复兴工委会作为临时办事处。中华独中之能够成功复办，并有今日的成就，丹华堂功不可没。 
        </p>    
        <p class="font-weight-bold">
            在文化活动方面，丹华堂由1989年开始，每逢农历八月，即举办庆祝中秋节的文化活动。丹华堂也曾多次联合商会，极力为华裔商家向州政府进言，解决华裔在回教化政策下所面对的部分难题。
        </p>    
        </div>

