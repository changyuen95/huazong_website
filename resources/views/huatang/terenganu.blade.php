        <h4 class="font-weight-bolder mb-3">简介</h4>
        <p class="font-weight-bold">
            登嘉楼中华大会堂成立于1976年。当年，拿督杜水淼担任中华总商会会长，在当年雪华堂会长丹斯里李延年的鼓励之下，发起组织大会堂，结果登高一呼，群山响应，热烈支持众多。1976年，登州各社团代表成立筹委会，随即向社团注册官申请注册，结果注册获准，首届会长由拿督杜水淼出任。
        </p>
        <p class="font-weight-bold">
            万事起头难，在创立初期，登华堂面对财务难题，直至拿督杜水淼当选连任会长之后，乃极力寻求打破财务困难的局面。1990年，大会堂主办精英文化义演，筹募建筑会馆基金，成绩斐然，使会馆的经济趋向稳定。同时，并拨出三十巴仙为全登州十间华小经费。
        </p>
        <p class="font-weight-bold">
            每年农历三月清明节，全体理事联同民间团体、青年组织及学生们参与布沙拉路旧华人义山的烈士纪念碑公祭。农历八月间，登华堂则举办中秋文化节，持续发扬华族传统文化。
        </p>
         
